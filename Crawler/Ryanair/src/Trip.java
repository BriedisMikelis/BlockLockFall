
public class Trip {
	String from;
	String to;
	String departuretime;
	String arrivalTime;
	String price;
	
	public Trip(String from, String to, String departuretime, String arrivalTime, String price) {
		super();
		this.from = from;
		this.to = to;
		this.departuretime = departuretime;
		this.arrivalTime = arrivalTime;
		this.price = price;
	}
}
